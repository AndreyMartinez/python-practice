# type_of_values.py
# Este archivo va a tratar sobre los tipos de valores en Python

# Los tipos de valores en Python son:
# - int: enteros
# - float: flotantes
# - str: strings
# - bool: booleanos
# - list: listas
# - tuple: tuplas
# - dict: diccionarios
# - set: conjuntos
# - None: None
# - range: range

# int
print(type(1))
# float
print(type(1.0))
# str
print(type("1"))
# bool
print(type(True))
# list
print(type([1, 2, 3]))
# tuple
print(type((1, 2, 3)))
# dict
print(type({"1": 1, "2": 2, "3": 3}))
# set
print(type({1, 2, 3}))
# None
print(type(None))
# range
print(type(range(1, 10)))
